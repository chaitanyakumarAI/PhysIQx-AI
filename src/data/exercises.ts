import type {
  Equipment,
  Exercise,
  ExerciseDifficulty,
  MuscleGroup,
} from "@/types/exercise";

/**
 * The exercise catalog — shown on Train's browse list, referenced by
 * Session's workout templates, and the plan builder's picker, so every
 * exercise means the same thing (and has the same id) everywhere.
 *
 * GENERATED from content/muscle-involvement_filled.json (author-provided,
 * EMG-informed stimulus shares — see that file's sourcing notes). Do not
 * hand-edit entries here; update the content file and regenerate. Grouped
 * by primary muscle; within a group the authored order stands (compounds
 * lead). muscleGroups derives from muscleHit, so involvement order and
 * percentages can never disagree.
 */
function ex(
  id: string,
  name: string,
  muscleHit: Partial<Record<MuscleGroup, number>>,
  equipment: Equipment,
  difficulty: ExerciseDifficulty,
): Exercise {
  const muscleGroups = (
    Object.entries(muscleHit) as [MuscleGroup, number][]
  )
    .sort((a, b) => b[1] - a[1])
    .map(([group]) => group);
  return { id, name, muscleHit, muscleGroups, equipment, difficulty };
}

